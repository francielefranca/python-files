estacao = input("Digite uma estacao:")

if estacao == "primavera":
    print("As flores estao nascendo.")
elif estacao == "verao":
    print("Ah, o calor! Mergulhe em uma piscina.")
elif estacao == "outono":
    print("As folhas estao caindo...")
elif estacao == "inverno":
    print("Chuvas e ventos.")
else:
    print("Voce nao digitou uma estacao :( ")